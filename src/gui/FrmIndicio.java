    private void btnActualizar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizar1ActionPerformed
        // TODO add your handling code here:
        if (txtFecha.getDate() == null) {
            JOptionPane.showMessageDialog(this, "Debes ingresar una fecha.", "Bufete", JOptionPane.WARNING_MESSAGE);
            txtFecha.requestFocus();
            return;
        }
        if (txtIndicio.getText().length() == 0) {
            JOptionPane.showMessageDialog(this, "Debes ingresar un indicio.", "Bufete", JOptionPane.WARNING_MESSAGE);
            txtIndicio.requestFocus();
            return;
        } else {
            String imagenActual = "";
            if (this.archivo.equals("")) {
                imagenActual = this.archivoAnterior;
            } else {
                imagenActual = this.archivo;
            }

            Object idTipoIndicio = cboTipoIndicio.getSelectedItem();
            int itemTpIndicio = ((TipoIndicio) idTipoIndicio).getId();

            java.util.Date fecha = txtFecha.getDate();
            java.sql.Date fechaSQL = new java.sql.Date(fecha.getTime());

            String respuesta = this.CONTROL.actualizar(Integer.parseInt(txtId.getText()), txtComentario.getText(), imagenActual, itemTpIndicio, fechaSQL, Integer.parseInt(txtIdCaso.getText()));
            if (respuesta.equals("OK")) {
                if (!this.archivo.equals("")) {
                    this.subirArchivo();
                }
                this.limpiar();
                this.listar("");
            }
            this.listar("");
        }
    }//GEN-LAST:event_btnActualizar1ActionPerformed

    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:
        JFileChooser file = new JFileChooser();
        int estado = file.showOpenDialog(this);
        if (estado == JFileChooser.APPROVE_OPTION) {
            this.archivo = file.getSelectedFile().getName();
            this.rutaOrigen = file.getSelectedFile().getAbsolutePath();
            this.rutaDestino = this.DIRECTORIO + this.archivo;

            txtIndicio.setText(this.archivo);
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

