    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if (txtFecha.getDate() == null) {
            JOptionPane.showMessageDialog(this, "Debes ingresar una fecha.", "Bufete", JOptionPane.WARNING_MESSAGE);
            txtFecha.requestFocus();
            return;
        } else {
            Object idTipoIndicio = cboTipoIndicio.getSelectedItem();
            int itemTpIndicio = ((TipoIndicio) idTipoIndicio).getId();


            java.util.Date fecha = txtFecha.getDate();
            java.sql.Date fechaSQL = new java.sql.Date(fecha.getTime());

            String respuesta = this.CONTROL.insertar(txtComentario.getText(), txtIndicio.getText(), itemTpIndicio, fechaSQL, Integer.parseInt(txtIdCaso.getText()));
            if (respuesta.equals("OK")) {
                if (!this.archivo.equals("")) {
                    this.subirArchivo();
                }
                this.limpiar();
                this.listar("");
            } 
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

