    private void btnActualizar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizar1ActionPerformed
        // TODO add your handling code here:
        if (tablaListado.getSelectedRowCount() == 1) {
            String idPago = String.valueOf(tablaListado.getValueAt(tablaListado.getSelectedRow(), 0));
            String nombreTipoPago = String.valueOf(tablaListado.getValueAt(tablaListado.getSelectedRow(), 1));
            String monto = String.valueOf(tablaListado.getValueAt(tablaListado.getSelectedRow(), 2));
            String fecha = String.valueOf(tablaListado.getValueAt(tablaListado.getSelectedRow(), 3));
            String comentario = String.valueOf(tablaListado.getValueAt(tablaListado.getSelectedRow(), 4));
            int idTipoPago = Integer.parseInt(String.valueOf(tablaListado.getValueAt(tablaListado.getSelectedRow(), 5)));

            txtId.setText(idPago);
            TipoPago seleccionado = new TipoPago(idTipoPago, nombreTipoPago);
            cboTipoPago.setSelectedItem(seleccionado);

            txtMonto.setText(monto);
            SimpleDateFormat formatoDelTexto = new SimpleDateFormat("yyyy-MM-dd");
            java.util.Date fechaN = null;
            try {
                fechaN = formatoDelTexto.parse(fecha);
                txtFecha.setDate(fechaN);
            } catch (Exception e) {
                e.printStackTrace();
            }
            
            Caso aux = CONTROL.listarTituloCaso(Integer.parseInt(idPago));
            
            txtIdBusqueda.setText(aux.getId()+"");
            txtResultadoBusqueda.setText(aux.getNombre());

            txtComentario.setText(comentario);

            panelGeneral.setEnabledAt(0, false);
            panelGeneral.setEnabledAt(1, true);
            panelGeneral.setSelectedIndex(1);
            this.accion = "editar";
            btnGuardar.setText("Editar");
        } else {
            this.mensajeError("Seleccione 1 registro a editar.");
        }
    }//GEN-LAST:event_btnActualizar1ActionPerformed

