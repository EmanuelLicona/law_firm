    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        try {
            String respuesta;
            if (txtMonto.getText().length() == 0) {
                this.mensajeError("Asegurate de ingresar un monto");
                txtMonto.requestFocus();
                return;
            }
            if (this.accion.equals("editar")) {
                if (txtFecha.getDate() == null) {
                    JOptionPane.showMessageDialog(this, "Fecha Vacia", "Bufete", JOptionPane.WARNING_MESSAGE);
                } else {
                    java.util.Date fecha = txtFecha.getDate();
                    java.sql.Date fechaSQL = new java.sql.Date(fecha.getTime());

                    Object item = cboTipoPago.getSelectedItem();
                    int itemTipoGasto = ((TipoGasto) item).getId();
                    respuesta = this.CONTROL.actualizar(Integer.parseInt(txtId.getText()), txtComentario.getText(), Double.parseDouble(txtMonto.getText()), fechaSQL, itemTipoGasto, Integer.parseInt(txtIdBusqueda.getText()));
                    if (respuesta.equals("OK")) {
                        this.limpiar();
                        this.listar();
                    }
                }
            } else {
                java.util.Date fecha = txtFecha.getDate();
                java.sql.Date fechaSQL = new java.sql.Date(fecha.getTime());

                Object item = cboTipoPago.getSelectedItem();
                int itemTipoGasto = ((TipoGasto) item).getId();
                respuesta = this.CONTROL.insertar(txtComentario.getText(), Double.parseDouble(txtMonto.getText()), fechaSQL, itemTipoGasto, Integer.parseInt(txtIdBusqueda.getText()));
                if (respuesta.equals("OK")) {
                    this.limpiar();
                    this.listar();
                }

            }
        } catch (Exception e) {
            mensajeError("Campos vacios");
        }
        
    }//GEN-LAST:event_btnGuardarActionPerformed

