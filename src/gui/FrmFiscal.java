    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if (estadoEditando) {
            try {
                if (permitirGuardar()) {
                    editar();
                    tblFiscal.setModel(fiscalControl.listar());

                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane, e.getMessage(),null,JOptionPane.WARNING_MESSAGE);
            }

        } else {

            try {
                if (permitirGuardar()) {
                    guardar();
                    tblFiscal.setModel(fiscalControl.listar());
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane, "ERROR EN FISCAL "+e.getMessage(),null,JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

