     public void ponerDatos(int id) {
        fiscalAux = fiscalControl.listandoUnRegistro(id);

        txtNombre.setText(fiscalAux.getNombre());
        txtApellido.setText(fiscalAux.getApellidos());
        txtCorreo.setText(fiscalAux.getCorreo());
        txtIdentidad.setText(fiscalAux.getIdentidad());
        txtTelefono.setText(fiscalAux.getTelefono());
        txtNumColegiado.setText(fiscalAux.getNum_colegiado() + "");
        
        if(fiscalAux.getEstado()==1){
            btnActivo.setSelected(true);
        }else{
            btnInactivo.setSelected(true);
        }

        
        for(EstadoCivil ec:new EstadoCivilDAO().listarGeneral()){
            if(ec.getId()==fiscalAux.getEstadoCivil()){
                comboEstadoCivil.setSelectedItem(ec.getNombre());
                break;
            }
        }
        
        for(TipoFiscal ec:new TipoFiscalDAO().listar()){
            if(ec.getId()==fiscalAux.getTipoFiscal()){
                comboTipoFiscal.setSelectedItem(ec.getNombre());
                break;
            }
        }
        
    }
     
     public void editar() {

        int estadoCivil = 0;

        for (EstadoCivil e : new EstadoCivilDAO().listarGeneral()) {
            if (e.getNombre().equals(comboEstadoCivil.getSelectedItem() + "")) {
                estadoCivil = e.getId();
                break;
            }
        }
        
        int tipoFical = 0;

        for (TipoFiscal e : new TipoFiscalDAO().listar()) {
            if (e.getNombre().equals(comboTipoFiscal.getSelectedItem() + "")) {
                tipoFical = e.getId();
                break;
            }
        }

        int estado = 0;

        if (btnActivo.isSelected()) {
            estado = 1;
        } else {
            estado = 0;
        }
        
        int num_colegiado= 0;
        
         try {
             num_colegiado = Integer.parseInt(txtNumColegiado.getText());
         } catch (Exception e) {
             num_colegiado = 0;
         }
        

        String resp = (fiscalControl.editar(fiscalAux.getIdFiscal(), txtNombre.getText(), txtApellido.getText(), txtIdentidad.getText(), txtTelefono.getText(), txtCorreo.getText(),
                num_colegiado,tipoFical, estadoCivil, estado));

         if(resp.equals("OK")){
             
            irFiscales();
        }
        

        fiscalAux = null;
    }
