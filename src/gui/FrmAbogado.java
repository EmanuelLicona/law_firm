    public void ponerDatos(int id) {
        abogadoAux = abogadoControl.listandoUnRegistro(id);

        txtNombre.setText(abogadoAux.getNombre());
        txtApellido.setText(abogadoAux.getPellidos());
        txtCorreo.setText(abogadoAux.getCorreo());
        txtDomicilio.setText(abogadoAux.getDomicilio());
        txtIdentidad.setText(abogadoAux.getNum_identidad());
        txtTelefono.setText(abogadoAux.getNum_telefono());
        txtFecha.setDate(abogadoAux.getFechaNacimiento());
        txtNumColegiado.setText(abogadoAux.getNum_colegiado() + "");

        if (abogadoAux.getEstado() == 1) {
            btnActivo.setSelected(true);
        } else {
            btnInactivo.setSelected(true);
        }

        DefaultTableModel model;

        String columnas[] = {"ID", "NOMBRE"};

        model = new DefaultTableModel(null, columnas);

        tblEspecialidad.setModel(model);

        //tblCategoria.setModel(categoriaAbogadoControl.listar());
        CategoriaAbogado c;
        for (int i : abogadoControl.listandoIdEspecialidad(id)) {
            c = categoriaAbogadoControl.listarUnRegistro(i);

            listarEspecialidad(c.getId() + "", c.getNombre(), tblEspecialidad);
        }

        for (EstadoCivil ec : new EstadoCivilDAO().listarGeneral()) {
            if (ec.getId() == abogadoAux.getEstadoCivil()) {
                comboEstadoCivil.setSelectedItem(ec.getNombre());
                break;
            }
        }
    }
