    private void btnAgregarEspActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarEspActionPerformed
        if (auxCategoria != null) {
            boolean existe = false;
            for (int i = 0; i < tblEspecialidad.getRowCount(); i++) {
                if (tblEspecialidad.getValueAt(i, 0).equals(auxCategoria.getId() + "")) {
                    existe = true;
                    break;
                }
            }

            if (!existe) {
                listarEspecialidad(auxCategoria.getId() + "", auxCategoria.getNombre(), tblEspecialidad);
                auxCategoria = null;
                tblCategoria.clearSelection();
            } else {
                JOptionPane.showMessageDialog(rootPane, "La categoria ya existe",null,JOptionPane.WARNING_MESSAGE);
            }

        } else {
            JOptionPane.showMessageDialog(rootPane, "Seleccione una categoria",null,JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnAgregarEspActionPerformed

