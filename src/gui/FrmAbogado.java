        for (EstadoCivil e : new EstadoCivilDAO().listarGeneral()) {
            if (e.getNombre().equals(comboEstadoCivil.getSelectedItem() + "")) {
                estadoCivil = e.getId();
                break;
            }
        }

        int estado = 0;

        if (btnActivo.isSelected()) {
            estado = 1;
        } else {
            estado = 0;
        }

        java.sql.Date date2;
        java.util.Date d = txtFecha.getDate();
        if (d != null) {
            date2 = new java.sql.Date(d.getTime());
        } else {
            date2 = null;
        }

        int num_cole;
        try {
            num_cole = Integer.parseInt(txtNumColegiado.getText());

        } catch (Exception e) {
            num_cole = 0;
        }

        String h = (abogadoControl.editar(abogadoAux.getIdAbogado(), txtNombre.getText(), txtApellido.getText(), txtIdentidad.getText(), txtTelefono.getText(), txtCorreo.getText(),
                txtDomicilio.getText(), date2, num_cole, estadoCivil, estado));

