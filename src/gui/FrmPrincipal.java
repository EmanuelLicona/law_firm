    public void ControlaInstancia(JInternalFrame inter) {

        boolean mostrar = true;
        String Nombre = inter.getTitle();
        for (int a = 0; a < escritorio.getComponentCount(); a++) {
            if (inter.getClass().isInstance(escritorio.getComponent(a))) {
                JOptionPane.showMessageDialog(rootPane, "La ventana que intenta abrir ya estÃ¡ abierta");
                //System.out.println("esta instancia, no se debe mostrar");
                inter.toFront();
                escritorio.moveToFront(inter);
                mostrar = false;

            } else {
                System.out.println("no lo es, puede mostrarse");
            }
        }
        if (mostrar) {
            if (this.getExtendedState() == JFrame.MAXIMIZED_BOTH) {
                inter.setBounds(0, 0, inter.getWidth(), inter.getHeight()-5);
            } else {
                 inter.setBounds(0, 0, inter.getWidth(), inter.getHeight()-5);
            }

            escritorio.add(inter);

        }
        inter.show();
    }

