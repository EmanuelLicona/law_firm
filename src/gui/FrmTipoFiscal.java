     public void guardar() {
            try {
                if(fiscalControl.insertar(txtNombre.getText(), txtDescripcion.getText() + "").equals("OK"))
                    limpiar();
            } catch (Exception e) {
                System.out.println(e.getMessage());
            }
    }

    public void actualizar() {

            try {
                int id;
                try {
                    id = Integer.parseInt(txtId.getText());
                } catch (Exception e) {
                    id = 0;
                }
                if(fiscalControl.actualizar(id, txtNombre.getText(), txtDescripcion.getText() + "").equals("OK"))
                     limpiar();
            } catch (Exception e) {
                System.out.println(e.getMessage());
            }
        
    }

    public void limpiar() {

        txtNombre.setText("");
        txtDescripcion.setText("");
        txtId.setText("");
        tblListado.setModel(fiscalControl.listar());
    }

    public void ponerDatos(int id) {

        TipoFiscal aux = new TipoFiscalControl().listarUnRegistro(id);
        if (aux != null) {
            txtNombre.setText(aux.getNombre());
            txtDescripcion.setText(aux.getDescripcion());
            txtId.setText(aux.getId() + "");
        }

    }
    
