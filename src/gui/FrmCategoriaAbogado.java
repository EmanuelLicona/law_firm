    public void guardar() {
            try {
                if(categoriaControl.insertar(txtNombre.getText(), txtDescripcion.getText() + "").equals("OK"))
                    limpiar();
            } catch (Exception e) {
                System.out.println(e.getMessage());
            }
    }

    public void actualizar() {
            try {
                int id;
                try {
                    id = Integer.parseInt(txtId.getText());
                } catch (Exception e) {
                    id = 0;
                }
                if(categoriaControl.actualizar(id, txtNombre.getText(), txtDescripcion.getText() + "").equals("OK"))
                     limpiar();
            } catch (Exception e) {
                System.out.println(e.getMessage());
            }
        
    }

    public void editar() {
        if (txtNombre.getText().length() < 1) {

            JOptionPane.showMessageDialog(rootPane, "INGRESE POR LO MENOS UN NOMBRE",null,JOptionPane.WARNING_MESSAGE);
        } else {
            try {
                // JOptionPane.showMessageDialog(rootPane, categoriaControl.insertar(txtNombre.getText(), txtDescripcion.getText()+""));
                limpiar();

            } catch (Exception e) {
                System.out.println(e.getMessage());
            }
        }
    }

    public void limpiar() {

        txtNombre.setText("");
        txtDescripcion.setText("");
        txtId.setText("");
        tblListado.setModel(categoriaControl.listar());
    }

    public void ponerDatos(int id) {

        CategoriaAbogado aux = new CategoriaAbogadoDAO().listarUnRegistro(id);
        if (aux != null) {
            txtNombre.setText(aux.getNombre());
            txtDescripcion.setText(aux.getDescripcion());
            txtId.setText(aux.getId() + "");
        }

    }

