    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        String respuesta;
        if (this.accion.equals("editar")) {
            Rol item = (Rol) cboRoles.getSelectedItem();
            respuesta = this.CONTROL.actualizar(Integer.parseInt(txtId.getText()), txtNombre.getText(), txtApellido.getText(), txtIdentidad.getText(), txtContrasenia.getText(), item.getId(), txtUsuario.getText(), this.usuarioAnt);
            if (respuesta.equals("OK")) {
                this.limpiar();
                this.listar("", false);
                panelGeneral.setSelectedIndex(0);
                panelGeneral.setEnabledAt(1, false);
                panelGeneral.setEnabledAt(0, true);
            } 
        } else {
            Rol item = (Rol) cboRoles.getSelectedItem();
            respuesta = this.CONTROL.insertar(txtNombre.getText(), txtApellido.getText(), txtIdentidad.getText(), txtContrasenia.getText(), item.getId(), txtUsuario.getText());
            if (respuesta.equals("OK")) {
                this.limpiar();
                this.listar("", false);
            }
        }

    }//GEN-LAST:event_btnGuardarActionPerformed

