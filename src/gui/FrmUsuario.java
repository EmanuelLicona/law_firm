    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        // TODO add your handling code here:
        if (tablaListado.getSelectedRowCount() == 1) {
            String id = String.valueOf(tablaListado.getValueAt(tablaListado.getSelectedRow(), 0));
            int rolId = Integer.parseInt(String.valueOf(tablaListado.getValueAt(tablaListado.getSelectedRow(), 1)));
            String nombreRol = String.valueOf(tablaListado.getValueAt(tablaListado.getSelectedRow(), 2));
            String nombre = String.valueOf(tablaListado.getValueAt(tablaListado.getSelectedRow(), 3));
            String apellido = String.valueOf(tablaListado.getValueAt(tablaListado.getSelectedRow(), 4));
            String identidad = String.valueOf(tablaListado.getValueAt(tablaListado.getSelectedRow(), 5));
            String clave = String.valueOf(tablaListado.getValueAt(tablaListado.getSelectedRow(), 6));
            String usuario = String.valueOf(tablaListado.getValueAt(tablaListado.getSelectedRow(), 8));
            this.usuarioAnt = String.valueOf(tablaListado.getValueAt(tablaListado.getSelectedRow(), 8));

            txtId.setText(id);
            Rol seleccionado = new Rol(rolId, nombreRol);
            cboRoles.setSelectedItem(seleccionado);
            txtNombre.setText(nombre);
            txtApellido.setText(apellido);
            txtIdentidad.setText(identidad);
            txtContrasenia.setText(clave);
            txtUsuario.setText(usuario);

            panelGeneral.setEnabledAt(0, false);
            panelGeneral.setEnabledAt(1, true);
            panelGeneral.setSelectedIndex(1);
            this.accion = "editar";
            btnGuardar.setText("Editar");
        } else {
            this.mensajeError("Seleccione 1 registro a editar.");
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

