    private void btnActualizar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizar1ActionPerformed
        // TODO add your handling code here:
        if (txtFecha.getDate() == null) {
            JOptionPane.showMessageDialog(this, "Campos vacios",null,JOptionPane.WARNING_MESSAGE);
            txtFecha.requestFocus();
            return;
        }if (txtEvidencia.getText().length() == 0) {
            JOptionPane.showMessageDialog(this, "Debes ingresar un indicio.", "Bufete", JOptionPane.WARNING_MESSAGE);
            txtEvidencia.requestFocus();
            return;
        } else {
            String imagenActual = "";
            if (this.archivo.equals("")) {
                imagenActual = this.archivoAnterior;
            } else {
                imagenActual = this.archivo;
            }

            Object idTipoEvidencia = cboTipoEvidencia.getSelectedItem();
            int itemTpEvidencia = ((TipoEvidencia) idTipoEvidencia).getId();

            java.util.Date fecha = txtFecha.getDate();
            java.sql.Date fechaSQL = new java.sql.Date(fecha.getTime());

            String respuesta = this.CONTROL.actualizar(Integer.parseInt(txtId.getText()), txtComentario.getText(), imagenActual, itemTpEvidencia, fechaSQL, Integer.parseInt(txtIdCaso.getText()));
            if (respuesta.equals("OK")) {
                if (!this.archivo.equals("")) {
                    this.subirArchivo();
                }
                this.limpiar();
                this.listar("");
            }
        }
    }//GEN-LAST:event_btnActualizar1ActionPerformed

