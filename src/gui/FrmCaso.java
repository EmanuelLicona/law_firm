    public void ponerDatos(int id) {

        Caso aux = new CasoDao().listandoUnRegistro(id);
        Cliente cc = new ClienteControl().listandoUnRegistro(aux.getIdCliente());

        txtCliente.setText(aux.getIdCliente() + "");
        lbNombreCliente.setText("" + cc.getNombre() + " " + cc.getApellidos());

        txtTitulo.setText(aux.getNombre());
        txtDescripcion.setText(aux.getDescripcion());
        txtExpDPI.setText(aux.getNumExpDPI() + "");
        txtExpJuz.setText(aux.getNumExpJuzgado() + "");
        txtExpMNP.setText(aux.getNumExpMinPublico() + "");
        txtHonorario.setText(aux.getHonorario() + "");
        txtFechaCreacion.setText(aux.getFechaCreacion() + "");
        txtJuzgado.setText(aux.getIdJuzgado() + "");

        lbJuzgado.setText(new JuzgadoDAO().listandoUnRegistro(aux.getIdJuzgado()).getNombre());

        if (aux.getEstado() == 1) {
            btnActivo.setSelected(true);
        } else {
            btnInactivo.setSelected(true);
        }

        for (TipoCaso ec : new TipoCasoDAO().listar()) {
            if (ec.getId() == aux.getIdTipoCaso()) {
                txtTipoCaso.setSelectedItem(ec.getNombre());
                break;
            }
        }

        for (Integer object : new CasoDao().listandoIdAbogados(aux.getId())) {
            anadiendoAbogado(new AbogadoControl().listandoUnRegistro(object), tblAbogado);
        }
    }

