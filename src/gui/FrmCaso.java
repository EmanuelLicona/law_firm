    private void btnAgregarAbogadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarAbogadoActionPerformed
        try {
            int id = Integer.parseInt(txtIdAbogado.getText());
            if (txtIdAbogado.getText().length() != 0) {

                boolean band = false;
                for (int i = 0; i < tblAbogado.getRowCount(); i++) {
                    if (tblAbogado.getValueAt(i, 0).equals(id + "")) {
                        band = true;
                    }
                }

                if (band) {
                    JOptionPane.showMessageDialog(rootPane, "Abogado ya esta en la lista",null,JOptionPane.ERROR_MESSAGE);
                    txtIdAbogado.setText("");
                    lbNombreAbogado.setText("");
                } else {
                    anadiendoAbogado(new AbogadoControl().listandoUnRegistro(id), tblAbogado);
                    txtIdAbogado.setText("");
                    lbNombreAbogado.setText("");
                }

            } else {
                JOptionPane.showMessageDialog(rootPane, "Campo id abogado vacio",null,JOptionPane.ERROR_MESSAGE);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Error campo id abogado",null,JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAgregarAbogadoActionPerformed

