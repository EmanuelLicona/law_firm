    private void btnBusquedaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBusquedaActionPerformed
        // TODO add your handling code here:
        if (txtIdBusqueda.getText().length() == 0) {
            this.mensajeError("Debes ingresar un ID para poder buscar");
        } else {
            String resultado = cboTipoBusqueda.getSelectedItem().toString();

            if (resultado.equals("Caso")) {
                this.llenarTextFieldCaso(Integer.parseInt(txtIdBusqueda.getText()));
            } else {
                //JOptionPane.showMessageDialog(rootPane, "entro");
                this.llenarTextFieldConsulta(Integer.parseInt(txtIdBusqueda.getText()));
            }

            if (txtResultadoBusqueda.getText().length() == 0) {
                mensajeError("REGISTRO NO EXISTE");
            }
        }
    }//GEN-LAST:event_btnBusquedaActionPerformed

    public boolean contieneLetra(String s) {
        for (int i = 0; i < s.length(); i++) {
            if (Character.isLetter(s.charAt(i))) {
                return true;
            }
        }

        return false;
    }

