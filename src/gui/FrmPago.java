    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:

        try {
            String respuesta;
            if (txtMonto.getText().length() == 0) {
                this.mensajeError("Asegurate de ingresar un monto");
                txtMonto.requestFocus();
                return;
            }
            
            
            if (txtIdBusqueda.getText().length() == 0) {
                this.mensajeError("Asegurate de asignar el pago");
                txtMonto.requestFocus();
                return;
            }

            Object item = cboTipoPago.getSelectedItem();
            int itemTipoPago = ((TipoPago) item).getId();

            String itemCombo = cboTipoBusqueda.getSelectedItem().toString();

            if (contieneLetra(txtMonto.getText())) {
                this.mensajeError("Campo monto contiene letras");
                return;
            }

            java.sql.Date fechaSQL;
            try {
                java.util.Date fecha = txtFecha.getDate();
                fechaSQL = new java.sql.Date(fecha.getTime());
            } catch (Exception e) {
                fechaSQL = null;
                this.mensajeError("Fecha no valida");
                return;
            }

            if (itemCombo.equals("Consulta")) {
                respuesta = this.CONTROL.insertar(txtComentario.getText(), Double.parseDouble(txtMonto.getText()), fechaSQL, itemTipoPago, 0, Integer.parseInt(txtIdBusqueda.getText()));
                if (respuesta.equals("OK")) {
                    this.limpiar();
                    this.listar();
                }
            } else {
                respuesta = this.CONTROL.insertar(txtComentario.getText(), Double.parseDouble(txtMonto.getText()), fechaSQL, itemTipoPago, Integer.parseInt(txtIdBusqueda.getText()), 0);
                if (respuesta.equals("OK")) {
                    this.limpiar();
                    this.listar();
                }
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage(), null, JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnGuardarActionPerformed

